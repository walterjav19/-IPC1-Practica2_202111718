    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String titulog= textField2.getText(); 
        String orden=(String)jComboBox2.getSelectedItem();
        String ruta= textField1.getText(); 
        Archivo admin = new Archivo();
        
        String text=admin.obtenerTexto(ruta);
        
        System.out.println(text);
        
        DefaultCategoryDataset datos= new DefaultCategoryDataset();
        
        Archivo a=new Archivo();
        String Cadena=a.obtenerTexto(ruta);
        String [] lineas=Cadena.split("\n");
        String[] encabezados=lineas[0].split(",");
        
        Dato[] datoz=new Dato[lineas.length-1];
        String[] columnas;
        for(int i=1;i<lineas.length;i++){
            columnas=lineas[i].split(",");
            
            datoz[i-1] = new Dato(columnas[0],columnas[1]);
        }
       
        for(Dato dato:datoz){
            datos.setValue(dato.getValor(),dato.getCategoria(),orden);
        }
        
        
        JFreeChart grafico= ChartFactory.createBarChart(titulog,encabezados[0],encabezados[1],datos,PlotOrientation.VERTICAL,true,true, false);
      
        panel =new ChartPanel(grafico);
        panel.setSize(702,271);
        
        jPanel2.setLayout(new BorderLayout());
        jPanel2.add(panel,BorderLayout.NORTH);
        
        pack();
       
        
        
        textField2.setText(null);
        textField1.setText(null);
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

         
